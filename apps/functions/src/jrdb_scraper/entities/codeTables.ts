/**
 * 系統コードを系統名に変換
 * @param code - 系統コード（4桁、例: "1101"）
 * @returns 系統名（例: "ノーザンダンサー系"）
 * @throws {Error} コードが見つからない場合
 */
export function convertKeitoCodeToName(code: string): string {
  const name = KEITO_CODE_MAP[code]
  if (!name) throw new Error(`Unknown keito code: ${code}. Valid codes: ${Object.keys(KEITO_CODE_MAP).join(', ')}`)
  return name
}

